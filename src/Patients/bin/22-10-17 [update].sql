CREATE TABLE Patients.MedicalSummaryFields (
	FieldID INT PRIMARY KEY IDENTITY NOT NULL,
	FieldTitle VARCHAR(50) UNIQUE NOT NULL,
	FieldDescription VARCHAR(250)
);

CREATE TABLE Patients.MedicalSummary (
	SummaryID INT PRIMARY KEY IDENTITY NOT NULL,
	PatientID INT,
	FieldID INT,
	SummaryText VARCHAR(500) NOT NULL,
	LastModified DATETIME NOT NULL DEFAULT GETDATE(),
	LastModifiedBy INT,
	FOREIGN KEY (PatientID) REFERENCES Patients.Patient(PatientID) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY (FieldID) REFERENCES Patients.MedicalSummaryFields(FieldID) ON UPDATE CASCADE ON DELETE SET NULL,
	FOREIGN KEY (LastModifiedBy) REFERENCES [Staffs].[Staff] (StaffID) ON UPDATE CASCADE ON DELETE SET NULL
);